public class GameEntity {    //Создать класс GameEntity (Игровая сущность)
    String name ;
    int health ;
    int damage;


    public GameEntity() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = this.name;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }


    }

